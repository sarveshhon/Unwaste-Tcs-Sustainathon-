package com.teamunwaste.unwaste.Home.HomeFM.slider;

public class HomeSliderModel {


    String image;
    String url;

    public HomeSliderModel() {}

    public HomeSliderModel(String image, String url) {
        this.image = image;
        this.url = url;
    }

    public String getImage() {
        return image;
    }

    public String getUrl() {
        return url;
    }
}
