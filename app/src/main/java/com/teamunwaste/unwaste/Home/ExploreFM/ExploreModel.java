package com.teamunwaste.unwaste.Home.ExploreFM;

public class ExploreModel {

    String title;
    String image;

    public ExploreModel() {

    }

    public ExploreModel(String title, String image) {
        this.title = title;
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public String getImage() {
        return image;
    }
}
